package simplify.bean;

public class SimplifyRequestHeadBean {

	private String envi;
	private String action;
	private String ip;
	public String getEnvi() {
		return envi;
	}
	public void setEnvi(String envi) {
		this.envi = envi;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	
}
